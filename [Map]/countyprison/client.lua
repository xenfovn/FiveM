key_to_teleport = 38

positions = {
    --[[
    {{Teleport1 X, Teleport1 Y, Teleport1 Z, Teleport1 Heading}, {Teleport2 X, Teleport 2Y, Teleport 2Z, Teleport2 Heading}, {Red, Green, Blue}, "Text for Teleport"}
    ]]
    {{1726.62, 2592.12, 44.36, 176.33}, {1720.26, 2566.51, 44.36, 128.27},{255,255,255}, ""}, -- Van binnenplaats naar algemene hal/kantine
    {{1724.98, 2583.08, 44.36, 269.92}, {1736.62, 2623.07, 44.36, 0,11},{255,255,255}, ""}, -- Van kantine hal naar prison gedeelte
    {{1730.92, 2565.78, 44.36, 358.50}, {1690.75, 2591.32, 44.36, 357.60},{255,255,255}, ""} -- Van Kantine hal naar buiten/ingang
}
